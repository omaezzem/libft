#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	result;
	int		sign;
	int		i;

	result = 0;
	sign = 1;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - 48);
		i++;
		if (sign == 1 && result >= 9223372036854775807 / 10)
			return (-1);
		else if (sign == -1 && result > 9223372036854775807 / 10)
			return (0);
	}
	return (result * sign);
}
